  <script>
    (function () {
      const form = document.getElementById('modernContactForm');
      const feedback = document.getElementById('formFeedback');
      const submitBtn = document.getElementById('submitBtn');

      // Real-time validation helper
      const validateField = (input) => {
        if (input.checkValidity()) {
          input.classList.remove('is-invalid');
          input.classList.add('is-valid');
          return true;
        } else {
          input.classList.remove('is-valid');
          input.classList.add('is-invalid');
          return false;
        }
      };

      // Attach input listeners
      Array.from(form.elements).forEach(el => {
        if (el.tagName.toLowerCase() === 'input' || el.tagName.toLowerCase() === 'textarea') {
          el.addEventListener('input', () => validateField(el));
        }
      });

      form.addEventListener('submit', (e) => {
        e.preventDefault();
        let allValid = true;
        Array.from(form.elements).forEach(el => {
          if ((el.tagName.toLowerCase() === 'input' || el.tagName.toLowerCase() === 'textarea') && el.hasAttribute('required')) {
            const valid = validateField(el);
            if (!valid) allValid = false;
          }
        });

        if (!allValid) {
          feedback.style.color = 'red';
          feedback.textContent = 'Please fix the errors above before submitting.';
          return;
        }

        // Since backend not yet hooked, show a placeholder success message
        feedback.style.color = 'green';
        feedback.textContent = 'All fields look good. Ready to send when backend is connected.';
      });
    })();
  </script>

