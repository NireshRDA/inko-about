           <h3 class="clik_h3 pt-5 pb-1">Curator:</h3>
           <p class="pt-2 pb-2">
           	<b>Nirmal Raja</b> is an interdisciplinary artist and curator living and working in Milwaukee, Wisconsin. In her work, she examines global movement and the cultural, material, and human legacies of colonialism. She approaches her art and curatorial practice as a means of inquiry and as a way of exercising citizenship. Raja feels an increasing responsibility towards furthering communication and understanding between cultures and she hopes to fulfill this through her curatorial practice. Her curatorial projects bring people from different cultures and backgrounds together. She believes in providing a platform for artists who explore notions of disenfranchisement, marginality, migration, globalization, and intersectional identity. Born in India, Raja lived in South Korea and Hong Kong before immigrating to the United States in 1991. She holds a BA in English Literature from St. Francis College in Hyderabad, India; a BFA from the Milwaukee Institute of Art and Design; and an MFA from the University of Wisconsin–Milwaukee. She has participated in solo and group shows in the Midwest, nationally, and internationally. Raja frequently collaborates with other artists and strongly believes in investing energy in her immediate community while also considering the global. Raja received the prestigious Mary L. Nohl fellowship for individual artists for 2021.
           	<br/><a href="http://www.nirmalraja.com/" target="_blank">http://www.nirmalraja.com/</a> 
           </p>
            <h3 class="clik_h3 pt-5 pb-1">Artists:</h3>
			<p class="pt-2">
			<b>Yeonhee Cheong</b> is a Madison, Wisconsin-based visual artist who studies human bodies as representations of the power relationship between the individual and the society. Inspired by the Wisconsin prairies and her life experiences as a woman, she explores the question of power often by means of the visual and/or physical texture of the botanical vocabulary that is deeply embedded in human visual culture. Born in South Korea and having started her career in the fashion industry, she takes a broader concept of human body as media, “the extensions of man,” including all the outfits, poses, even the mode of representation itself (the “media”), particularly in this era of social media based on electronic signals after  the pandemic. She earned fashion design degrees from Seoul National University and from the Fashion Institute of Technology in New York, and an MFA in Design Studies from the University of Wisconsin-Madison. Her works have been nationally and internationally shown through the Surface Design Association, International Textile and Apparel Association, Women’s Caucus for Art, Indianapolis Art Center, Madison Museum of Contemporary Art, and Overture Center for the Arts, among others. Cheong has received several honors including most recently an award from Women Artists Forward Fund and a juried commission for the Madison Public Library.<br/><a href="https://yeonheecheong.com" target="_blank">https://yeonheecheong.com</a>
			</p>
          	<p class="pt-2">
          	<b>Yoonshin Park</b> is a Chicago-based multimedia artist, curator, and educator working with sculptural papers, artist books, and installations. Her interest in the comprehensive processes of papermaking and bookbinding leads her work to encompass various elements woven into complete objects. Born in Seoul, she often uses her experience as a foreign transplant to question space and its implications in defining identity. Park has shown at the Ukrainian Institute of Modern Art, Hyde Park Art Center, Bridgeport Art Center, and Art on Armitage in Chicago. She has also exhibited at FlexSpace at the Riverside Art Center, Pablo Center at the Confluence and the AIR Space at Saint Kate–The Arts Hotel in Wisconsin, and Simyo Gallery in Seoul, among other venues. She has curated projects at Adds Donna, Blink Contemporary Art, and Woman Made Gallery. Park received her MFA in Interdisciplinary Book and Paper Arts from Columbia College Chicago. Her work has been reviewed in the June 2019 issue of Sculpture magazine. She currently teaches at Hyde Park Art Center and Evanston Art Center.<br/><a href="https://www.yoonshinpark.com" target="_blank">https://www.yoonshinpark.com</a>
          	</p>
            <p class="pt-2">
            <b>Rina Yoon</b> is a Korean-born visual artist and a professor of Fine Art at the Milwaukee Institute of Art and Design in Wisconsin. Yoon received a BFA in Fine Art from Southern Methodist University in Dallas and an MFA in Printmaking from Washington University in St. Louis. She focuses on nontraditional printmaking methods including large-scale prints, paper installations, and multimedia work combining video and sculptural elements with printmaking. Driven by curiosity and sensitivity to materials, Yoon often embraces slow processes to allow time for reflection and meditation. Her work has been widely exhibited in the United States as well as in South Korea, China, India, Italy, and Poland.<br/><a href="http://www.rinayoon.com" target="_blank">http://www.rinayoon.com</a>
			</p>
